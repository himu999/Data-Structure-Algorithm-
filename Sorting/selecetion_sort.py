def merge_sort(lst):

    for i in range(len(lst) - 1):
        min_index = i

        for j in range(min_index + 1, len(lst)):
            if str(lst[min_index]) > str(lst[j]):
                min_index = j

        if i != min_index:
            lst[min_index], lst[i] = lst[i], lst[min_index]

    return lst


